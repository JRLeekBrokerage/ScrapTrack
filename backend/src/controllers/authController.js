const jwt = require('jsonwebtoken');
const bcrypt = require('bcryptjs');
const User = require('../models/User');
const { validationResult } = require('express-validator');

// Generate JWT token
const generateToken = (userId) => {
  return jwt.sign(
    { userId },
    process.env.JWT_SECRET || 'scraptrack-secret-key',
    { expiresIn: process.env.JWT_EXPIRES_IN || '24h' }
  );
};

// Generate refresh token
const generateRefreshToken = (userId) => {
  return jwt.sign(
    { userId, type: 'refresh' },
    process.env.JWT_REFRESH_SECRET || 'scraptrack-refresh-secret',
    { expiresIn: process.env.JWT_REFRESH_EXPIRES_IN || '7d' }
  );
};

// Register new user
const register = async (req, res) => {
  try {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const { username, email, password, firstName, lastName, role, phone } = req.body;

    // Check if user already exists
    const existingUser = await User.findOne({
      $or: [{ email }, { username }]
    });

    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: existingUser.email === email ? 'Email already registered' : 'Username already taken'
      });
    }

    // Create new user
    const user = new User({
      username,
      email,
      password,
      firstName,
      lastName,
      role: role || 'driver',
      phone
    });

    // Set default permissions based on role
    switch (user.role) {
      case 'admin':
        user.permissions = [
          { module: 'freight', actions: ['create', 'read', 'update', 'delete', 'export'] },
          { module: 'invoicing', actions: ['create', 'read', 'update', 'delete', 'export'] },
          { module: 'reports', actions: ['create', 'read', 'update', 'delete', 'export'] },
          { module: 'users', actions: ['create', 'read', 'update', 'delete', 'export'] },
          { module: 'drivers', actions: ['create', 'read', 'update', 'delete', 'export'] },
          { module: 'scrap', actions: ['create', 'read', 'update', 'delete', 'export'] },
          { module: 'settings', actions: ['create', 'read', 'update', 'delete'] }
        ];
        break;
      case 'manager':
        user.permissions = [
          { module: 'freight', actions: ['create', 'read', 'update', 'export'] },
          { module: 'invoicing', actions: ['read', 'update', 'export'] },
          { module: 'reports', actions: ['read', 'export'] },
          { module: 'drivers', actions: ['read', 'update', 'export'] },
          { module: 'scrap', actions: ['create', 'read', 'update', 'export'] }
        ];
        break;
      case 'dispatcher':
        user.permissions = [
          { module: 'freight', actions: ['create', 'read', 'update'] },
          { module: 'drivers', actions: ['read', 'update'] },
          { module: 'scrap', actions: ['create', 'read', 'update'] }
        ];
        break;
      case 'accountant':
        user.permissions = [
          { module: 'invoicing', actions: ['create', 'read', 'update', 'export'] },
          { module: 'reports', actions: ['read', 'export'] }
        ];
        break;
      case 'driver':
        user.permissions = [
          { module: 'freight', actions: ['read'] },
          { module: 'scrap', actions: ['read'] },
          { module: 'reports', actions: ['read'] }
        ];
        break;
    }

    await user.save();

    // Generate tokens
    const token = generateToken(user._id);
    const refreshToken = generateRefreshToken(user._id);

    // Update last login
    user.lastLogin = new Date();
    await user.save();

    res.status(201).json({
      success: true,
      message: 'User registered successfully',
      data: {
        user: {
          id: user._id,
          username: user.username,
          email: user.email,
          firstName: user.firstName,
          lastName: user.lastName,
          fullName: user.fullName,
          role: user.role,
          permissions: user.permissions,
          isActive: user.isActive,
          lastLogin: user.lastLogin
        },
        token,
        refreshToken
      }
    });
  } catch (error) {
    console.error('Registration error:', error);
    res.status(500).json({
      success: false,
      message: 'Registration failed',
      error: process.env.NODE_ENV === 'development' ? error.message : undefined
    });
  }
};

// Login user
const login = async (req, res) => {
  try {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const { login, password } = req.body;

    // Find user by email or username
    const user = await User.findOne({
      $or: [{ email: login }, { username: login }],
      isActive: true
    });

    console.log('User found by login identifier:', user ? user.toObject() : null); // Log the found user

    if (!user) {
      console.log('Login attempt: No active user found for identifier:', login);
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials'
      });
    }

    // Check password
    const isPasswordValid = await user.comparePassword(password);
    if (!isPasswordValid) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials'
      });
    }

    // Generate tokens
    const token = generateToken(user._id);
    const refreshToken = generateRefreshToken(user._id);

    // Update last login
    user.lastLogin = new Date();
    await user.save();

    res.json({
      success: true,
      message: 'Login successful',
      data: {
        user: {
          id: user._id,
          username: user.username,
          email: user.email,
          firstName: user.firstName,
          lastName: user.lastName,
          fullName: user.fullName,
          role: user.role,
          permissions: user.permissions,
          isActive: user.isActive,
          lastLogin: user.lastLogin
        },
        token,
        refreshToken
      }
    });
  } catch (error) {
    console.error('Login error:', error);
    res.status(500).json({
      success: false,
      message: 'Login failed',
      error: process.env.NODE_ENV === 'development' ? error.message : undefined
    });
  }
};

// Refresh token
const refreshToken = async (req, res) => {
  try {
    const { refreshToken } = req.body;

    if (!refreshToken) {
      return res.status(401).json({
        success: false,
        message: 'Refresh token required'
      });
    }

    const decoded = jwt.verify(
      refreshToken,
      process.env.JWT_REFRESH_SECRET || 'scraptrack-refresh-secret'
    );

    if (decoded.type !== 'refresh') {
      return res.status(401).json({
        success: false,
        message: 'Invalid refresh token'
      });
    }

    const user = await User.findById(decoded.userId);
    if (!user || !user.isActive) {
      return res.status(401).json({
        success: false,
        message: 'Invalid refresh token'
      });
    }

    const newToken = generateToken(user._id);
    const newRefreshToken = generateRefreshToken(user._id);

    res.json({
      success: true,
      message: 'Token refreshed successfully',
      data: {
        token: newToken,
        refreshToken: newRefreshToken
      }
    });
  } catch (error) {
    console.error('Token refresh error:', error);
    res.status(401).json({
      success: false,
      message: 'Invalid refresh token'
    });
  }
};

